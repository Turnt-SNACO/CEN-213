package src;

public class Flower {
	private String name, color;
	//Constructor
	public Flower(String name, String color){
		this.name=name;		this.color=color;
	}
	//Returns name as a string
	public String getName() {
		return name;
	}
	//Returns color as a string
	public String getColor() {
		return color;
	}	
}
